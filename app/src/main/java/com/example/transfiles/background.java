package com.example.transfiles;

public class background {
    private int ValueR;
    private int ValueG;
    private int ValueB;
    public background(int _ValueR, int _ValueG, int _ValueB)
    {
        ValueR = _ValueR;
        ValueG = _ValueG;
        ValueB = _ValueB;
    }

    public int getValueR() {
        return ValueR;
    }
    public int getValueG() {
        return ValueG;
    }
    public int getValueB() {
        return ValueB;
    }
}
